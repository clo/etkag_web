<h3>Firmengeschichte</h3>
<table class=normal border=0>
<tr><td>
Die Firma wurde von Jean Kalbermatter 1972 als Einzelunternehmen gegründet. 
Jean kämpfte sich in den ersten Jahren mit kaufmännischer Unterstützung in das 
Geschäftsleben. Durch die langjährige Arbeitserfahrung von Jean bei Von Roll 
und Habegger bekam er die ersten Aufträge im Erstellen von Seilbahn und 
Kransteuerungen. Hier konnte er sich gute Referenzen schaffen und sein 
Tätigkeitsbereich im Elektro-Tableaubau erweitern. So konnte Jean regelmässig 
zwei Arbeiter beschäftigen.<br>
Im Jahre 1986 begann sein Sohn Frank Kalbermatter die Lehre als Schaltanlagenmonteur. 
Anschliessend an die Lehre sammelte Frank Berufserfahrung bei Elektro Wicky AG und bei 
der damaligen FO in Brig. Berufsbegleitend absolvierte Frank die Meisterschule, welche 
er 1995 abschloss.<br>
Aus der Einzelunternehmung wurde 1994 eine Aktiengesellschaft. Durch die 
innovative Weiterentwicklung der Firma Jean Kalbermatter AG wurde 1996 die 
jetzige Firmenlokalität gekauft. Die grosszügige Werkstatthalle wurde für einen 
speditiven Tableaubau eingerichtet.
Da sich die Firma stetig verbessern und bestätigen will, wurden die Geschäftsgänge 
vom QM-Verantwortlichen Frank überdacht und im QM-Handbuch klar definiert. 
So erlangte die Firma 2002 die ISO-Zertifizierung 9001-2000.<br>
Mit der Schaffung eines Arbeitsplatzes für die Automation im Jahre 2003 können 
wir ergänzend zum Elektro-Tableaubau
die verschiedensten Know-How Lösungen für Elektro-Steuerungen anbieten. 
Wir sind im Stande technisch, fach- und zeitgerechte Lösungen auszuführen, von der 
Aufnahme des Pflichtenheftes, Erstellung des Elektroschemas, Produktion vom Elektrotableau, 
Erstellung der SPS Software, Inbetriebnahme vor Ort bis zum Service / Unterhalt.<br>
Ab dem Jahre 2005 ist Frank Kalbermatter für die Geschäftsführung verantwortlich.
Während den Jahren 2005/2006 wurde der ganze Betriebs- und Auftragsablauf optimiert. 
Alle unsere Arbeitsprozesse, inklusive Administrationsaufwand, können wir mit einer 
eigens auf unseren Betrieb abgestimmten Auftragssoftware einfach abwickeln und überwachen. 
Alle Kontakte, Artikel, Dienstleistungen, Arbeitsprozesse und Arbeitspapiere sind 
in der Software mit den wichtigsten Informationen hinterlegt. Auch die Werkzeughaltung 
wurde genau definiert und festgehalten und verhindert unnötigen, ärgerlichen Zeitverlust. 
Zusätzlich wurden alle wichtigen Artikel in einem Zeichnungsprogramm erfasst. 
Dadurch können wir schnell und flexibel Zeichnungen mit gutem Erkennungswert der 
Elektrotableaus erstellen. Mit den Kunden können wir dank diesen Zeichnungen die 
letzten Details  und Anpasswünsche besprechen. Die Fertigungszeiten wurden dank diesen 
Zeichnungen um mehr als ¼ reduziert. Spätere Erweiterungen oder Anpassungen sind 
dank den hinterlegten Zeichnungen viel einfacher.<br>
Mit dem neuen Firmennamen ETK Elektro-Tableau Kalbermatter AG mit dem Zusatz 
Schaltanlagen und Automation wurde 2006 ein neues, einheitliches Auftreten 
definiert, welches unsere Tätigkeit besser beschreibt.
</td></tr>
